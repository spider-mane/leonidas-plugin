const mix = require('laravel-mix');
const yargs = require('yargs');
const argv = yargs(process.argv.slice(2))
  .options({
    openBrowser: {type: 'boolean', default: false},
  })
  .parseSync();

const root = '..';
const vendor = `${root}/vendor`;
const src = 'src';
const dist = 'dist';

mix

  /**
   *==========================================================================
   * Output directory
   *==========================================================================
   *
   *
   *
   */
  .setPublicPath('dist')

  /**
   *==========================================================================
   * Sourcemaps
   *==========================================================================
   *
   *
   *
   */
  .sourceMaps(true, 'eval-source-map', 'source-map')

  /**
   *==========================================================================
   * Versioning
   *==========================================================================
   *
   *
   *
   */
  .version()

  /**
   *==========================================================================
   * Browsersync
   *==========================================================================
   *
   *
   *
   */
  .browserSync({
    proxy: 'leonidas-plugin.test',
    open: argv.open ?? false,
    notify: false,
    logLevel: 'debug',
    files: [
      'dist/**/*.js',
      'dist/**/*.css',
      '../app/**/*.php',
      '../boot/**/*.php',
      '../theme/**/*.php',
      '../config/**/*.php',
      '../views/**/*.twig',
    ],
  })

  /**
   *==========================================================================
   * Javascript
   *==========================================================================
   *
   *
   *
   */
  // .js('src/js/index.js', 'dist/js/script.js')
  // .autoload({jquery: ['$', 'window.jQuery']})
  // .extract()

  /**
   *==========================================================================
   * Typescript
   *==========================================================================
   *
   *
   *
   */
  .ts('src/ts/index.ts', 'dist/js/script.js')
  // .autoload({jquery: ['$', 'window.jQuery']})
  .extract()

  /**
   *==========================================================================
   * Sass
   *==========================================================================
   *
   *
   *
   */
  .sass('src/scss/main.scss', 'dist/css/styles.css', {
    sassOptions: {
      outputStyle: 'expanded',
    },
  })

  /**
   *==========================================================================
   * Copies
   *==========================================================================
   *
   *
   *
   */
  .copy([], 'dist/lib/')

  /**
   *==========================================================================
   * Options
   *==========================================================================
   *
   *
   *
   */
  .options({
    processCssUrls: false,
    // imgLoaderOptions: {},
  })

  /**
   *==========================================================================
   * Webpack
   *==========================================================================
   *
   *
   *
   */
  .webpackConfig({
    stats: {
      children: true,
    },
  });
